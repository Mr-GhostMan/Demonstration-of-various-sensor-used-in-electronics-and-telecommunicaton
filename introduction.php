<H2>Sensor</H2>
<div class="container">
<p>We live in a World of Sensors. You can find different types of Sensors in our homes, offices, cars etc. working to make our lives easier by turning on the lights by detecting our presence, adjusting the room temperature, detect smoke or fire, make us delicious coffee, open garage doors as soon as our car is near the door and many other tasks. All these and many other automation tasks are possible because of Sensors.</p>
<p>A <b>sensor</b> is a device or module, or subsystem whose purpose is to detect events or changes in its environment and send the information to other electronics,frequently to a computer processor. Just put cursor on sensor you will note the changes!!!</p>
<p> The block diagram of sensor is given below.</p>
<p>There are several classifications of sensors made by different authors and experts. Some are very simple and some are very complex. The following classification of sensors may already be used by an expert in the subject but this is a very simple classification of sensors.</p>
<p>In the first classification of the sensors, they are divided in to Active and Passive. Active Sensors are those which require an external excitation signal or a power signal.</p>
<p>Passive Sensors, on the other hand, do not require any external power signal and directly generates output response.</P>
<p>The other type of classification is based on the means of detection used in the sensor. Some of the means of detection are Electric, Biological, Chemical, Radioactive etc.</p>
<p>The next classification is based on conversion phenomenon i.e. the input and the output. Some of the common conversion phenomena are Photoelectric, Thermoelectric, Electrochemical, Electromagnetic, Thermooptic, etc.</p>
